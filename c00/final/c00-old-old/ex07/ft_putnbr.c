#include <unistd.h>

void	print(int nb)
{
	int	arr[11];
	int	i;

	i = 0;
	while (nb > 0)
	{
	arr[i] = nb % 10 + '0';
	nb /= 10;
	i++;
	}
	while (--i >= 0)
	{	
		write(1, &arr[i], 1);
	}
}

void	ft_putnbr(int nb)
{	
	if (nb == 0)
	{
		write(1, "0", 1);
	}
	else if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0 || nb != -2147483648)
		{
			write(1, "-", 1);
			nb = nb * -1;
		}
		print(nb);
	}
}
